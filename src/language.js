function changeLanguage(language) {
    switch (language) {
        case 'ru':
        window.location.href = './index_ru.html';
        break;
        case 'uk':
        window.location.href = 'index.html';
        break;
        default:
          window.location.href = 'index.html';
          break;
      }      
  }

  
  
  